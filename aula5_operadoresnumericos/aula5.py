"""
Operadores Numéricos:
+(adição), -(subtração), *(multiplicação), /(divisão), //(divisão inteira)
**(exponenciação), %(resto da divisão), () (dar preferência no calculo)
"""
print('Multiplicação *:',  10 * 10)
print('Repetição *:',  10 * "gabriel")
print('Adição +:',  10 + 10)
print('Concatenação +:',  'gabriel' + 'bonati')
print('Subtração -:', 10 - 5)
print('Divisão /:', 100/3)
print('Divisão inteira //:', 100 // 3)
print('exponenciação **:', 5 ** 3)
print('resto %:', 15 % 4)
print('Dar Preferência ():', (10 + 5) * 8)
